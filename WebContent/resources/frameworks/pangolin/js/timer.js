/**
 * 获取当前年份
 * */
function getCurrentYear() {
	var currentDate = new Date();
	return currentDate.getFullYear();
}

/**
 * 获取当前月份
 * */
function getCurrentMonth() {
	var currentDate = new Date();
	return currentDate.getMonth()+1;
}

/**
 * 获取当前日
 * */
function getCurrentDay() {
	var currentDate = new Date();
	return currentDate.getDate();
}

/**
 * 获取年份范围
 * */
function getYearRange() {
	var years = new Array();
	var currentYear = getCurrentYear();
	for ( var i = 0 ; i <= 9; i ++ ) {
		years.push(currentYear + i);
	}
	return years;
}

/**
 * 加载年份选择器
 * */
function loadYearSelector(selector, initialValue) {
	var years = getYearRange();
	for ( var i = 0; i < years.length; i ++ ) {
		selector.append( "<option value=\""+years[i]+"\">" + years[i] + "</option>" );
	}
	if ( initialValue != undefined ) selector.val(initialValue);
}

/**
 * 加载月份选择器
 * */
function loadMonthSelector(selector, initialValue) {
	for ( var i = 1; i <= 12; i ++ ) {
		selector.append( "<option value=\"" + i + "\">" + i + "" + "</option>" );
	}
	if ( initialValue != undefined ) selector.val(initialValue);
}

/**
 * 加载日选择器
 * */
function loadDaySelector(currentYear, currentMonth, selector, initialValue) {
	selector.empty();
    if ( currentYear == "" || currentMonth == "" || currentYear == "-" || currentMonth == "-" ) {
    	selector.append( "<option value=\"\">-</option>" );
    	return;
    }
    
    var  max = new Date(currentYear, currentMonth, 0).getDate();
    for( var i = 1; i <= max; i++ ) {
    	selector.append( "<option value=\"" + i + "\">" + i + "</option>" );
    }
    
    if ( initialValue != undefined ) selector.val(initialValue);
}